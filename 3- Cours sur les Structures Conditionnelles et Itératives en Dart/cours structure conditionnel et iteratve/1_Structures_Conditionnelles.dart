/**
 * 1. Structures Conditionnelles (if, else, else if) :
 *Les structures conditionnelles permettent d'exécuter des blocs de code en fonction de conditions.
 */

void main() {
  int nombre = 10;

  if (nombre > 0) {
    print("$nombre est positif");
  } else if (nombre < 0) {
    print("$nombre est négatif");
  } else {
    print("$nombre est nul");
  }
}
