/**
 * 8. Boucles forEach :
 *Les boucles forEach permettent d'itérer sur une liste ou un ensemble de valeurs.
 * 
 */

void main() {
  List<String> fruits = ["Pomme", "Banane", "Orange"];

  fruits.forEach((fruit) {
    print("Fruit : $fruit");
  });
}


