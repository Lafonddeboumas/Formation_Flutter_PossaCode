/**
 * 6. Méthodes et Propriétés Statiques :
 * Les méthodes et les propriétés statiques appartiennent à la classe plutôt qu'à une instance
 *  spécifique.
 */

class MathUtils {
  static const double pi = 3.14;

  static double calculerCirconference(double rayon) {
    return 2 * pi * rayon;
  }
}
