/*---------------------------------- 4. Opérateurs d'Assignation :  -----------------------------*/

//Les opérateurs d'assignation sont utilisés pour attribuer une valeur à une variable.

void main() {
  int a = 5;

  // Opérateurs d'assignation combinée
  a += 2; // Équivaut à a = a + 2
  a -= 3; // Équivaut à a = a - 3
  a *= 4; // Équivaut à a = a * 4

  print("Valeur de a après opérations : $a");
}
