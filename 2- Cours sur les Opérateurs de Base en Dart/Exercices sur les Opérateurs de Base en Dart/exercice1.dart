 *Écrivez un programme qui prend deux nombres en entrée et affiche le résultat des opérations 
 *arithmétiques suivantes : addition, soustraction, multiplication, division, et reste de la division.
 */

void main() {
  int a = 10;
  int b = 3;

  print("Addition : ${a + b}");
  print("Soustraction : ${a - b}");
  print("Multiplication : ${a * b}");
  print("Division : ${a / b}");
  print("Reste de la division : ${a % b}");
}
