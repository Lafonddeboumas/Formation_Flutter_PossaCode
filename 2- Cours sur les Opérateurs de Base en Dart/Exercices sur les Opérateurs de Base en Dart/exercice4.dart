/**
 *Exercice 4 : Opérateurs d'Assignation
 *Écrivez un programme qui utilise des opérateurs d'assignation combinée pour modifier la valeur
 *d'une variable.
 */

void main() {
  int a = 5;

  a += 3; // Équivaut à a = a + 3
  a *= 2; // Équivaut à a = a * 2

  print("Valeur de a après opérations : $a");
}
