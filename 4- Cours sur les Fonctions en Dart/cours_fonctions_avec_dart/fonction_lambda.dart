/**
 * 7. Fonctions Anonymes (Lambda) :
 * Les fonctions anonymes, également appelées fonctions lambda, peuvent être utilisées
 *  pour créer des fonctions de manière concise.
 */

void main() {
  var addition = (int a, int b) => a + b;
  print("La somme est : ${addition(3, 5)}");
}


